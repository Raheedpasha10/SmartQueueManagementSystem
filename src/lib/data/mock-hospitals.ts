/**
 * Filter hospitals by location (within radius)
 */
export function filterHospitalsByLocation(
  hospitals: MockHospital[],
  latitude: number,
  longitude: number,
  radiusMeters: number
): MockHospital[] {
  return hospitals.filter(hospital => {
    const distance = calculateDistance(
      latitude,
      longitude,
      hospital.latitude,
      hospital.longitude
    );
    return distance <= radiusMeters;
  });
}

/**
 * Calculate distance between two coordinates (in meters)
 */
function calculateDistance(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number
): number {
  const R = 6371000; // Earth's radius in meters
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

/**
 * Search hospitals by text query
 */
export function searchHospitalsByText(query: string): MockHospital[] {
  const lowerQuery = query.toLowerCase();
  return MOCK_HOSPITALS.filter(
    hospital =>
      hospital.name.toLowerCase().includes(lowerQuery) ||
      hospital.city.toLowerCase().includes(lowerQuery) ||
      hospital.specialties.some(s => s.toLowerCase().includes(lowerQuery))
  );
}

